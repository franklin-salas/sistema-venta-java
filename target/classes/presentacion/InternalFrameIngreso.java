    private void crearDetalle() {
        String[] titulos = {"Id", "CÃ³digo", "Articulo", "Cantidad", "Precio", "Subtotal"};

        this.modeloDetalle = new DefaultTableModel(null, titulos) {
            @Override
            public boolean isCellEditable(int row, int column) {
                // Hacer que todas las celdas no sean editables

                return column == 3 || column == 4;
            }

            @Override
            public Object getValueAt(int row, int column) {

                if (column == 5) {
                    Double cantidad;
                    Double precio;
                    try {
                        String textCant = String.valueOf(getValueAt(row, 3));
                        String textprecio = String.valueOf(getValueAt(row, 4));
                        cantidad = Double.valueOf(textCant);
                        precio = Double.valueOf(textprecio);
                    } catch (NumberFormatException e) {
                        cantidad = 1.0;
                        precio = 0.0;
                    }
                    return String.format(Locale.US, "%.2f", cantidad * precio);
                }
                return super.getValueAt(row, column);
            }

            @Override
            public void setValueAt(Object aValue, int row, int column) {
                super.setValueAt(aValue, row, column);
                calcularTotales();
                fireTableDataChanged();

            }
        };

        this.tablaDetalle.setModel(modeloDetalle);
    }

    private void calcularTotales() {

        int count = this.tablaDetalle.getRowCount();
        double total = 0;
        double subTotal = 0;

        for (int i = 0; i < count; i++) {
            String textSubTotal = String.valueOf(modeloDetalle.getValueAt(i, 5));
            total = total + Double.parseDouble(textSubTotal);
        }

        subTotal = total / (1 + Double.parseDouble(textImpuesto.getText()));

        this.textSubtotal.setText(String.format(Locale.US, "%.2f", subTotal));
        this.textTotalImpuesto.setText(String.format(Locale.US, "%.2f", total - subTotal));
        this.textTotal.setText(String.format(Locale.US, "%.2f", total));

    }

    private void listenerEdit() {
        ListSelectionModel selectionModel = tabla.getSelectionModel();
        selectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    boolean filaSeleccionada = !selectionModel.isSelectionEmpty() && tabla.getSelectedRowCount() == 1;

                    btnAnular.setEnabled(false);
                    btnVer.setEnabled(false);

                    if (filaSeleccionada) {
                        int filaSeleccionadaIndex = tabla.getSelectedRow();

                        id = String.valueOf(tabla.getValueAt(filaSeleccionadaIndex, 0));

                        //String tipoPersona =String.valueOf(tabla.getValueAt(filaSeleccionadaIndex,1));
                        String estado = String.valueOf(tabla.getValueAt(filaSeleccionadaIndex, 11));

                        btnVer.setEnabled(true);

                        if (!estado.equals("Anulado")) {
                            btnAnular.setEnabled(true);
                        }

                    }
                }

            }
        });

    }

    private void paginar() {
        int totalPaginas;

        this.totalRegistros = this.control.total();
        this.totalPorPagina = Integer.parseInt((String) cboTotalPorPagina.getSelectedItem());
        totalPaginas = (int) (Math.ceil((double) this.totalRegistros / this.totalPorPagina));
        if (totalPaginas == 0) {
            totalPaginas = 1;
        }
        cboNumPagina.removeAllItems();

        for (int i = 1; i <= totalPaginas; i++) {
            cboNumPagina.addItem(Integer.toString(i));
        }
        cboNumPagina.setSelectedIndex(0);
    }

    private void listar(String text, boolean paginar) {

        this.totalPorPagina = Integer.parseInt((String) cboTotalPorPagina.getSelectedItem());
        if ((String) cboNumPagina.getSelectedItem() != null) {
            this.numPagina = Integer.parseInt((String) cboNumPagina.getSelectedItem());
        }

        if (paginar == true) {
            tabla.setModel(this.control.listar(text, this.totalPorPagina, this.numPagina));
        } else {
            tabla.setModel(this.control.listar(text, this.totalPorPagina, 1));
        }

        //this.tabla.setModel(this.control.listar(text, totalPorPagina, numPagina));
        TableRowSorter orden = new TableRowSorter(this.tabla.getModel());
        this.tabla.setRowSorter(orden);

        this.labelTotalRegistros.setText("Mostrando " + this.control.totalMostrados() + " de " + this.control.total());

    }

