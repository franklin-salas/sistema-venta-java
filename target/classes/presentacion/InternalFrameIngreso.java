    private void btnAnularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnularActionPerformed
        // TODO add your handling code here:
        if (tabla.getSelectedRowCount() == 1) {
            int filaSeleccionadaIndex = tabla.getSelectedRow();
            String tipoDocumento = String.valueOf(tabla.getValueAt(filaSeleccionadaIndex, 5));
            String serie = String.valueOf(tabla.getValueAt(filaSeleccionadaIndex, 6));
            String numero = String.valueOf(tabla.getValueAt(filaSeleccionadaIndex, 7));
            if (JOptionPane.showConfirmDialog(this, "Â¿Desea anular el registro: " + tipoDocumento + " " + serie + " - " + numero, "Anular Registro", JOptionPane.YES_NO_CANCEL_OPTION) == 0) {
                String res = this.control.anular(Integer.parseInt(id));
                if (res.equals("OK")) {
                    this.listar("", false);
                } else {
                    this.mensajeError(res);
                }
            }
        }
    }//GEN-LAST:event_btnAnularActionPerformed


