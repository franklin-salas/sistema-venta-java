    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        if (textIdProveedor.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un proveedor.", "Sistema", JOptionPane.WARNING_MESSAGE);
            btnSeleccionarProveedor.requestFocus();
            return;
        }
        
        
        if (textSerieComprobante.getText().length() > 7) {
            JOptionPane.showMessageDialog(this, "Debes ingresar una serie no mayor a 7 caracteres.", "Sistema", JOptionPane.WARNING_MESSAGE);
            textSerieComprobante.requestFocus();
            return;
        }
        if (textNumeroSerie.getText().length() == 0 || textNumeroSerie.getText().length() > 10) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un nÃºmero de comprobante no mayor a 10 caracteres.", "Sistema", JOptionPane.WARNING_MESSAGE);
            textNumeroSerie.requestFocus();
            return;
        }
        if (modeloDetalle.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Debes agregar artÃ­culos al detalle.", "Sistema", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String resp = "";
        resp = this.control.insertar(Integer.parseInt(textIdProveedor.getText()), (String) cboTipoComprobante.getSelectedItem(), textSerieComprobante.getText(), textNumeroSerie.getText(), Double.parseDouble(textImpuesto.getText()), Double.parseDouble(textTotal.getText()), modeloDetalle);
        if (resp.equals("OK")) {
            this.mensajeSuccess("Registrado correctamente");
            this.limpiarFormulario();
            this.tabla.clearSelection();
            this.listar("", false);
            this.tabCrud.setEnabledAt(1, false);
            this.tabCrud.setEnabledAt(0, true);
            this.tabCrud.setSelectedIndex(0);
        } else {
            this.mensajeError(resp);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

