    public void addDetalleArticulo(Articulo art) {

        int existe = -1;
        for (int i = 0; i < this.modeloDetalle.getRowCount(); i++) {
            String valor = String.valueOf(modeloDetalle.getValueAt(i, 0));
            // System.out.println("articulo :"+ valor);
            // System.out.println(modeloDetalle.getValueAt(i, 0));
            int idArt = Integer.parseInt(valor);
            if (idArt == art.getId()) {
                existe = i;
                System.out.println("existe ");
                break;
            }
        }

        if (existe == -1) {
            this.modeloDetalle.addRow(new Object[]{art.getId(), art.getCodigo(), art.getNombre(), "1", art.getPrecio(), art.getPrecio()});
            // this.tablaDetalle.setModel(this.modeloDetalle);
            // this.modeloDetalle.fireTableDataChanged();
            calcularTotales();
            return;
        }

        String valor = String.valueOf(modeloDetalle.getValueAt(existe, 3));
        int stock = Integer.parseInt(valor);

        this.modeloDetalle.setValueAt(stock + 1, existe, 3);

        calcularTotales();

    }

    @Override
    public void seleccionarObjetoDialog(Object aObjeto, int tipoObjeto) {

        switch (tipoObjeto) {
            case SelectorInterface.ARTICULO:
                this.addDetalleArticulo((Articulo) aObjeto);
                break;
            case SelectorInterface.PROVEEDOR:
                Persona proveedor = (Persona) aObjeto;
                this.textIdProveedor.setText(String.valueOf(proveedor.getId()));
                this.textNombreProveedor.setText(proveedor.getNombre());
                break;
            default:
                throw new AssertionError();
        }

    }

