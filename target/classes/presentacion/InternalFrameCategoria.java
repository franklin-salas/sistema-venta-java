    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (this.textNombre.getText().length() == 0) {
            this.mensajeWarning("El campo nombre es obligatorio.");
            this.textNombre.requestFocus();
            return;
        }

        if (this.textNombre.getText().length() > 20) {
            this.mensajeWarning("El campo nombre es demasiado largo, max = 20 caracteres.");
            this.textNombre.requestFocus();
            return;
        }

        if (this.textDescripcion.getText().length() > 255) {
            this.mensajeWarning("El campo descripcion es demaciado largo, max = 255 caracteres.");
            this.textDescripcion.requestFocus();
            return;
        }

        String res = "";
        if (this.accion.equals("EDITAR")) {

            res = this.control.actualizar(Integer.parseInt(id), this.textNombre.getText(), this.nombreAnt, this.textDescripcion.getText());
            this.tabCategorias.setEnabledAt(1, false);
            this.tabCategorias.setEnabledAt(0, true);
            this.tabCategorias.setSelectedIndex(0);
        } else {
            res = this.control.insertar(this.textNombre.getText(), this.textDescripcion.getText());
        }

        if (res.equals("OK")) {
            this.mensajeSuccess("Registrado correctamente.");
            this.limpiarFormulario();
            this.tablaCategoria.clearSelection();
            this.listar("");
        } else {
            this.mensajeError(res);
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

